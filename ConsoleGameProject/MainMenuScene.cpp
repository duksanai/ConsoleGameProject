void MainMenuScene::SelectMode()
{
    int key;
    while (true)
    {
        system("cls");
        PrintMainMenu();
        key = _getch();
        if (key != 0)
        {
            system("cls");
            deckSelectScene.SelectDeck();
        }
    }
}

